/*
[Ch1-1] printf와 scanf를 대신하는 입출력 방식
문제 2.
프로그램 사용자로부터 이름과 전화번호를 문자열의 형태로 입력 받아서,
입력 받은 데이터를 그대로 출력하는 프로그램을 작성해 보자.
*/
#include <iostream>
int main(void) {
	char name[10];
	char phone[20];
	std::cout << "이름을 입력하세요." << std::endl;
	std::cin >> name;
	std::cout << "전화번호를 입력하세요." << std::endl;
	std::cin >> phone;
	std::cout << "이름: " << name<<std::endl;
	std::cout << "전화번호: " << phone << std::endl;
	return 0;
}